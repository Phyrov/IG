
class Vertice{
    public:
        float x;
        float y;
        float z;

        Vertice()
        {
            x=0;
            y=0;
            z=0;
        }

        Vertice(float x, float y, float z)
        {
            this->x = x;
            this->y = y;
            this->z = z;
        }

    private:
};
